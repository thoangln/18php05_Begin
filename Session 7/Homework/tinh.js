function Sumab() {
	var a= document.getElementById("a").value;
    var b= document.getElementById("b").value;
	document.getElementById("kq").value = parseInt(a) + parseInt(b);
}
function Subab() {
	var a= document.getElementById("a").value;
    var b= document.getElementById("b").value;
	document.getElementById("kq").value = parseInt(a) - parseInt(b);
}
